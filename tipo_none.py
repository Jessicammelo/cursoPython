"""
O tipo None no Python
representa o tipo sem tipo, oupoderia ser conhecido tambem como tipo vazio, falar que é
um tipo sme tipo é mais apropriado!

Obs: O tipo none é sempre especificado com a primeira letra maiuscula. (None)

Podemos utilizar none quando queremos criar uma variavel e inicializala com um tipo sme tipo,
 antes de recebemosum valor final
"""
